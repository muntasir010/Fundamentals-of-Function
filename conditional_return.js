function isEven(number){
    if(number % 2 === 0){
        return true;
    }
    else{
        return false;
    }
}
console.log(isEven(50));
console.log(isEven(105));

function isOdd(number){
    if(number % 2 === 0){
        return true;
    }
    else{
        return false;
    }
}
console.log(isOdd(10));
console.log(isOdd(7));